package br.com.fiap.bean;

public class Bicicleta {
	private String marcaBike, marcaCambio, modeloBike, tipoFreio, numMarchas, pedal, cambio, pneu, freio, roda, quadro;
	private float tamanhoQuadro;
	
	// Getters e Setters
	public String getMarcaBike() {
		return marcaBike;
	}
	public void setMarcaBike(String marcaBike) {
		this.marcaBike = marcaBike;
	}
	public String getMarcaCambio() {
		return marcaCambio;
	}
	public void setMarcaCambio(String marcaCambio) {
		this.marcaCambio = marcaCambio;
	}
	public String getModeloBike() {
		return modeloBike;
	}
	public void setModeloBike(String modeloBike) {
		this.modeloBike = modeloBike;
	}
	public String getTipoFreio() {
		return tipoFreio;
	}
	public void setTipoFreio(String tipoFreio) {
		this.tipoFreio = tipoFreio;
	}
	public String getNumMarchas() {
		return numMarchas;
	}
	public void setNumMarchas(String numMarchas) {
		this.numMarchas = numMarchas;
	}
	public String getPedal() {
		return pedal;
	}
	public void setPedal(String pedal) {
		this.pedal = pedal;
	}
	public String getCambio() {
		return cambio;
	}
	public void setCambio(String cambio) {
		this.cambio = cambio;
	}
	public String getPneu() {
		return pneu;
	}
	public void setPneu(String pneu) {
		this.pneu = pneu;
	}
	public String getFreio() {
		return freio;
	}
	public void setFreio(String freio) {
		this.freio = freio;
	}
	public String getRoda() {
		return roda;
	}
	public void setRoda(String roda) {
		this.roda = roda;
	}
	public String getQuadro() {
		return quadro;
	}
	public void setQuadro(String quadro) {
		this.quadro = quadro;
	}
	public float getTamanhoQuadro() {
		return tamanhoQuadro;
	}
	public void setTamanhoQuadro(float tamanhoQuadro) {
		this.tamanhoQuadro = tamanhoQuadro;
	}

		
}
